#Import Pipeline for Sentiment analysis
from transformers import pipeline
classifier = pipeline("sentiment-analysis")
classifier("This is the worst day of my life")

#Sentiment Analysis for Two or More Sentences
classifier(
    ['this product is good but not satisfaction', 'good day', 'bad day', "very poor"]
)

#Named Entity Recognition Using Pipeline
ner_pipeline = pipeline("ner", grouped_entities=True)
result = ner_pipeline("Barack Obama was born in Hawaii.")
print(result)

#Zero-Shot-Classification using pipeline
classifier1 = pipeline("zero-shot-classification")
classifier1(
    "I am Working on new project",
    candidate_labels = ["AI", "Frontend", "backend"]
)

#Text Generation using Pipeline
generator = pipeline("text-generation", model="gpt2-medium")
generator("APJ most commonly refers to Avul Pakir Jainulabdeen Abdul Kalam, the 11th President")

